package crypto

import (
	"crypto/rand"
	"file-sharing/config"
	"math/big"
)

func CreateToken() string {
	const (
		length  = config.TOKEN_LENGTH
		charset = "abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789"
	)

	b := make([]byte, length)
	for i := range b {
		n, _ := rand.Int(rand.Reader, big.NewInt(int64(len(charset))))
		b[i] = charset[n.Int64()]
	}
	return string(b)
}
